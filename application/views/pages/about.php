	<h1>¿Qué es VideoBox?</h1>
	<p>VideoBox es una plataforma de servicios de streaming
	   que permite a sus usuarios subir contenidos multimedia y
	   ponerlos al alcance de clientes que deseen reproducirlos.</p>
	<h1 style="padding-top:10px;">¿Cómo funciona VideoBox?</h1>
	<p>VideoBox incluye una aplicación web que facilita la visualización
		de los vídeos subidos mediante streaming convencional o
		a través del estándar MPEG-DASH.
		La aplicación web actúa de intermediario entre el usuario y
		el sistema, pudiendo codificar un vídeo en diferentes calidades
		de reproducción mediante previa autenticación.</p>
	<h1 style="padding-top:10px;">Funciones de VideoBox:</h1>
	<ul>
	    <li><p>Transcodificación de archivos: codifica tus materiales de vídeo
		utilizando el códec más soportado en la web, el estándar H.264.</p></li>
	    <li><p>Almacenamiento: una vez transcodificados los vídeos, VideoBox
		los almacena para que puedan ser difundidos por el servidor de streaming Wowza.</p></li>
	    <li><p>Reproducción inmediata y de calidad: los reproductores usados
		para visualizar los contenidos permiten disfrutar de una experiencia única.</p></li>
	</ul>
